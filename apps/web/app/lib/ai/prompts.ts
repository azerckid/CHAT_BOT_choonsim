/** '춘심' 명칭을 실제 캐릭터 이름으로 변환 */
export function applyCharacterName(instruction: string, name: string): string {
    if (!name || name === '춘심') return instruction;
    return instruction
        .replace(/춘심이/g, name)
        .replace(/춘심/g, name);
}

